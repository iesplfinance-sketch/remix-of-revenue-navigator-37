// Complete data structure for school revenue simulation
// Data extracted from provided spreadsheet images

export interface ClassData {
  className: string;
  renewalCount: number;
  renewalFee: number;
  newAdmissionCount: number;
  newAdmissionFee: number;
  // Optional: Direct forecasted counts (overrides slider-based growth calculation)
  forecastedRenewalCount?: number;
  forecastedNewCount?: number;
  // Optional: Max capacity per class (used for over-capacity highlighting & full capacity revenue)
  maxCapacity?: number;
}

export interface CampusData {
  id: string;
  name: string;
  shortName: string;
  classes: ClassData[];
  maxCapacity: number;
  discountRate: number; // Forecasted discount rate
  lastYearDiscount: number; // Last year's discount rate (for current year calculations)
  newStudentGrowth: number; // Default 0%
  renewalGrowth: number; // Default 0%
  newAdmissionFeeHike: number; // Default 0%
  renewalFeeHike: number; // Default 0%
  annualFeeApplicable: boolean; // Whether annual fee applies to this campus
}

export interface CustomFee {
  id: string;
  name: string;
  amountPerStudent: number;
  appliesToSchool: boolean;
  appliesToHostel: boolean;
}

export interface HostelData {
  id: string;
  name: string;
  campusId: string;
  currentOccupancy: number;
  maxCapacity: number;
  feePerStudent: number; // Forecasted hostel fee per student
  lastYearFeePerStudent: number; // Last year hostel fee (default 375,000)
}

